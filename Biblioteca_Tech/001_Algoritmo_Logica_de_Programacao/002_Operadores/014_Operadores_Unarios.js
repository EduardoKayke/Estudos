/**
 * Operadores unários
 * 
 * ! = Negação Lógica é um operador unário.
 * 
 *  a + b = infix, operador no meio dos operandos.
 *  a++ = postfix, operador após os operandos.
 *  ++a = prefix. operador antes dos operandos.
*/

let a = 1;

a = a + 1;
a += 1;
a++; // ++ Operador Unário. 
++a;
a--;
--a;

console.log(a); // a = 4