// Também temos o operador módulo.

// Módulo ( % ) = Resto da divisão. Sempre dará 1 ou 0.
// 1 ou 0 para ímpar e par restantes de divisões.
let resto = 10 % 3;
console.log(resto); // 1

let restoPar = 10323243438 % 2;
console.log(restoPar); // 0