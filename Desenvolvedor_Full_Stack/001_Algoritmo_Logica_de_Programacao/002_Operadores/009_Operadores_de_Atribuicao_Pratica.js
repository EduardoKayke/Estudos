// Atribuição é o símbolo de igual.

// =
// +=
// -=
// *=
// /=
// %=

let x = 10;
x = x + 6;
x = x - 1;

let y = 20;
y += 10; // Atribuição aditiva.
y -= 2; // Atribuição subtrativa.
y *= 2; // Atribuição multiplicativa.
y /= 4; // Atribuição divisiva.
y %= 2; // Atribuição módulo.

console.log(x);
console.log(y);

let w = 'Texto'; 
w += ', mais texto.';

