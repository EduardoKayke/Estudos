var num1 = 42; var num2 = 19; nome = `Eduardo`;

// Soma.
var resultado = num1 + num2;
console.log(`O resultado é ${resultado}. Seu nome é ${nome}`);

// Subtração.
resultado = num1 - num2;
console.log(`O resultado é ${resultado}.`);

// Multiplicação.
resultado = num1 * num2;
console.log(`O resultado é ${resultado}.`);

// Divisão.
resultado = num1 / num2;
console.log(`O resultado é ${resultado}.`);

// Módulo. Resto da divisão.
resultado = num2 % 2;
console.log(`O resultado é ${resultado}.`);

// Incremento e decremento.
num1 += 8; // +=, -=, *=, /=
console.log(`${num1}`);

